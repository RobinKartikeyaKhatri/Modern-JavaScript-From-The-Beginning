// Primitive Types


// String
const name = "John Doe";
console.log(typeof name);

// Number
const age = 45;
console.log(typeof age);

// Boolean
const hasKids = true;
console.log(typeof hasKids);

// Null
const car = null;
console.log(typeof car);

// Undefined
let test;
console.log(typeof test);

// Symbol
const sym = Symbol();
console.log(typeof sym);



// Reference Types

// Array
const hobbies = ["movies", "music"];
console.log(typeof hobbies);

// Object Literal
const address = {
    city: "Barmer",
    state: "Rajasthan",
    country: "India",
    pincode: 344001
}
console.log(typeof address);

// Date
const today = new Date();
console.log(today);
console.log(typeof today);